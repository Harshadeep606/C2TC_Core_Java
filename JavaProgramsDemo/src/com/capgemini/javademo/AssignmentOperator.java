package com.capgemini.javademo;

public class AssignmentOperator {

	public static void main(String[] args) {
		int number = 4;
		number += 5; // x = x + 5
		number -= 5; // x = x - 5
		number *= 5; // x = x * 5
		number /= 4; // x = x / 4
		number %= 5; // x = x % 5
		System.out.println(number);

	}

}
